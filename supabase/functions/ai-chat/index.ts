import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are an intelligent assistant for AyurVeda Practice Manager — an Ayurvedic healthcare management app.

You help with TWO areas:

1. **App Usage Help**: Guide users on how to navigate the app:
   - Dashboard: Overview of patient stats, dosha distribution, recent patients
   - Patients: View, add, edit patient records with details like name, age, gender, height, weight, BMI, dosha type, health issues
   - Patient Detail: View full patient info, diet plans, progress charts, download PDF summary
   - Appointments (Admin only): Schedule appointments with date, time, and notes
   - Settings: Change display name, password, toggle dark mode
   - Roles: Admins see all patients; Customers see only their own

2. **Ayurvedic Knowledge**: Answer questions about:
   - The three doshas: Vata, Pitta, Kapha — their characteristics, imbalances, remedies
   - Ayurvedic diet principles, seasonal routines (Ritucharya), daily routines (Dinacharya)
   - Common Ayurvedic herbs, treatments, and lifestyle recommendations
   - BMI interpretation in Ayurvedic context

Be concise, friendly, and helpful. Use bullet points for clarity. If unsure, say so honestly.`;

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add funds." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
